package bwa;
use Moo::Role;

##-----------------------------------------------------------
##---------------------- ATTRIBUTES -------------------------
##-----------------------------------------------------------

##-----------------------------------------------------------
##------------------------ METHODS --------------------------
##-----------------------------------------------------------

sub bwa_index {
    my $self = shift;
    $self->pull;

    my $config = $self->options;
    my $opts   = $self->tool_options('bwa_index');

    my $cmd = sprintf( "%s/bwa -a %s index %s %s\n",
        $config->{bwa}, $opts->{a}, $config->{fasta} );
    $self->bundle( \$cmd );
}

##-----------------------------------------------------------

1;
